# Script to show colors in bash

# source: https://stackoverflow.com/questions/5947742/how-to-change-the-output-color-of-echo-in-linux

# Colors on output
black=`tput setaf 0`
red=`tput setaf 1`
green=`tput setaf 2`
yellow=`tput setaf 3`
blue=`tput setaf 4`
magenta=`tput setaf 5`
cyan=`tput setaf 6`
white=`tput setaf 7`

reset=`tput sgr0`
